// MARK: - Quest Lookup Helpers
export function getQuestById(id: string): Quest | undefined {
  return allQuests.find(q => q.id === id);
}

export function getQuestsByCategory(category: Quest['category']): Quest[] {
  return allQuests.filter(q => q.category === category);
}

export function getQuestsByRoom(room: Quest['room']): Quest[] {
  return allQuests.filter(q => q.room === room);
}

export function getQuestsByMaxDuration(maxDuration: number): Quest[] {
  return allQuests.filter(q => q.duration <= maxDuration);
}
